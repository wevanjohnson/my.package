#' This is my subtract function
#'
#' @param x this is the first value
#' @param y this is the second value to subtract
#'
#' @return This function returns the difference of x and y
#'
#' @examples
#' ## Start with something simple
#' subtract(1,1)
#'
#' ## Now something more difficult
#' subtract(49,60)
#'
#' @export
subtract <- function(x,y){x-y}
