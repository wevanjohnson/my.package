#' This is my multiplication function 'multiply'
#'
#' @param a this is the first value to add
#' @param b this is the second value to add
#'
#' @return This function presents the multiplication of a and b
#' @examples
#' ## 
#' a<-10
#' b<-50
#' print (multiply(a,b))
#'[1] 500
#' @export

multiply <- function(a,b){a*b}
print (multiply(a,b))