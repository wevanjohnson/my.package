#' This is my division function
#'
#' @param x this is the first value
#' @param y this is the second value
#'
#' @return This function returns the division of x and y
#'
#' @examples
#' ## Start with something simple
#' divide(1,1)
#'
#' ## Now something more difficult
#' divide(14,7)
#'
#' @export

divide <- function(x,y){x/y}
