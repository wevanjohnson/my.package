#' This is my multiplication function
#'
#' @param x this is the first value
#' @param y this is the second value
#'
#' @return This function returns the multiplication of x and y
#'
#' @examples
#' ## Start with something simple
#' multiply(1,1)
#'
#' ## Now something more difficult
#' multiply(2,7)
#'
#' @export

multiply <- function(x,y){x*y}
