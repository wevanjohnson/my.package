#' This is my multiplication function
#'
#' @param x this is the first value to add
#' @param y this is the second value to add
#'
#' 
#'
#' @examples
#' ## Start with something simple
#' multi(1,1)
#'
#' ## Now something more difficult
#' multi(50,50)
#'
#' @export

multi <- function(x,y){x*y}
multi(1,1)
multi(50,50)

